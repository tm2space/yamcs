package org.yamcs.booking.provider;

/**
 * Common ground station model used across all providers
 */
public class ProviderGroundStation {
    private String id;           // Provider's ground station ID
    private String name;         // Ground station name
    private String city;
    private String state;
    private String country;
    private double latitude;
    private double longitude;

    public String getId() { return id; }
    public void setId(String id) { this.id = id; }

    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public String getCity() { return city; }
    public void setCity(String city) { this.city = city; }

    public String getState() { return state; }
    public void setState(String state) { this.state = state; }

    public String getCountry() { return country; }
    public void setCountry(String country) { this.country = country; }

    public double getLatitude() { return latitude; }
    public void setLatitude(double latitude) { this.latitude = latitude; }

    public double getLongitude() { return longitude; }
    public void setLongitude(double longitude) { this.longitude = longitude; }
}
